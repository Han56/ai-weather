package com.han56.weather.models.entity;

public class PotraitSettingInfo {

    private String openId;

    private String gender;

    private String ageGroup;

    private String countryRegion;

    private String ethnicity;

    private String heightRange;

    private String weightRange;

    private String clothingStyle;

    private String accessoriesPreference;

    private String hairstylePreference;

    private Integer adCode;


    public String getOpenId() {
        return openId;
    }

    public void setOpenId(String openId) {
        this.openId = openId;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAgeGroup() {
        return ageGroup;
    }

    public void setAgeGroup(String ageGroup) {
        this.ageGroup = ageGroup;
    }

    public String getCountryRegion() {
        return countryRegion;
    }

    public void setCountryRegion(String countryRegion) {
        this.countryRegion = countryRegion;
    }

    public String getEthnicity() {
        return ethnicity;
    }

    public void setEthnicity(String ethnicity) {
        this.ethnicity = ethnicity;
    }

    public String getHeightRange() {
        return heightRange;
    }

    public void setHeightRange(String heightRange) {
        this.heightRange = heightRange;
    }

    public String getWeightRange() {
        return weightRange;
    }

    public void setWeightRange(String weightRange) {
        this.weightRange = weightRange;
    }

    public String getClothingStyle() {
        return clothingStyle;
    }

    public void setClothingStyle(String clothingStyle) {
        this.clothingStyle = clothingStyle;
    }

    public String getAccessoriesPreference() {
        return accessoriesPreference;
    }

    public void setAccessoriesPreference(String accessoriesPreference) {
        this.accessoriesPreference = accessoriesPreference;
    }

    public String getHairstylePreference() {
        return hairstylePreference;
    }

    public void setHairstylePreference(String hairstylePreference) {
        this.hairstylePreference = hairstylePreference;
    }

    public Integer getAdCode() {
        return adCode;
    }

    public void setAdCode(Integer adCode) {
        this.adCode = adCode;
    }
}
